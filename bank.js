class bank{
    constructor(amount){
        this.amount = amount
    }
    deposite(amount) {
        this.amount = this.amount + amount
    }
    withdrow(amount) {
        this.amount = this.amount - amount
    }
    getBalance() {
        console.log("Balance is" + this.amount)
    }
}

var myobj = new bank(1000)
console.log(myobj.amount)
myobj.deposite(8000)
myobj.getBalance()
myobj.withdrow(5000)
myobj.getBalance()


